int32_t hmac_get_cali_data_send_len(wlan_h2d_cali_data_type_enum_uint8 data_type)
{
    uint8_t rf_num = hmac_get_rf_num();
    int32_t data_len;

    switch (data_type) {
        case WLAN_NORMAL_CHN:
            data_len =  sizeof(wlan_cali_chn_stru) * rf_num;
            break;
        case WLAN_DBDC_CHN:
            data_len =  sizeof(wlan_cali_chn_stru) * (rf_num >> 1);
            break;
        case WLAN_20M_ALL_CHN:
            data_len =  sizeof(wlan_cali_rf_20m_para_stru) * rf_num;
            break;
        default:
            data_len =  sizeof(wlan_cali_data_20m_common_para_stru);
            break;
    }
    return data_len;
}
